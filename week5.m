\f0\fs24 \cf0 T/F The VC-dimension is the maximum number of points that can be shattered in an infinite number of ways. Explain.\
\
False, the VC-dimension refers to the maximum number of points that can be shattered *at all*. Not all sets of points can be shattered in an infinite number of ways i.e. there may be only one way to shatter some number of points, but that doesn\'92t change it\'92s dimensionality.\
